class RedHeadDuck implements SwimBehavior, FlyBehavior, QuackBehavior {
    public void swim() {
        System.out.println("RedHead Duck swims");
    }

    public void fly() {
        System.out.println("RedHead Duck flies");
    }

    public void quack() {
        System.out.println("RedHead Duck quacks");
    }
}
